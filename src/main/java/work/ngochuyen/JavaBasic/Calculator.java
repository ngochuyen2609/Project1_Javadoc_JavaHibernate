package work.ngochuyen.JavaBasic;

/**
 * Lớp này thực hiện các phép tính toán học cơ bản.
 * @author CaoThiNgocHuyen
 * @version 1.0
 */
public class Calculator {

    /**
     * Phuong thuc cong hai so nguyen
     *
     * @param a so hang thu nhat
     * @param b so hang thu hai
     *
     * @return Tong cua a va b
     * */
    public int add(int a, int b) {
        return a + b;
    }

    /**
     * Phuong thuc tru hai so nguyen
     *
     * @param a So bi tru
     * @param b So tru
     * @return Hieu cua a va b
     */
    public int subtract(int a, int b) {
        return a - b;
    }
}
